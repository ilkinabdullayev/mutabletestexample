package info.abdullayev.cook.cake;

public class Cake {

    private int margarine;
    private int flour;
    private int cocoa;
    private int eggs;
    private int orangeJuice;
    private int sugar;

    public void setMargarine(int margerine) {
        this.margarine = margerine;
    }

    public int getMargarine() {
        return margarine;
    }

    public void setFlour(int flour) {
        this.flour = flour;
    }

    public int getFlour() {
        return flour;
    }

    public void setCocoa(int cocoa) {
        this.cocoa = cocoa;
    }

    public int getCocoa() {
        return cocoa;
    }

    public void setEggs(int eggs) {
        this.eggs = eggs;
    }

    public int getEggs() {
        return eggs;
    }

    public void setOrangeJuice(int orangeJuice) {
        this.orangeJuice = orangeJuice;
    }

    public int getOrangeJuice() {
        return orangeJuice;
    }

    public void setSugar(int sugar) {
        this.sugar = sugar;
    }

    public int getSugar() {
        return sugar;
    }

}
